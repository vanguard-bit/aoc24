def part_2():
    ans = 0
    data = input.split('\n')
    for i in range(1, len(data) - 1):
        for j, letter in enumerate(data[i][1:-1], 1):
            if letter == 'A':
                if (
                    data[i - 1][j - 1] == data[i - 1][j + 1] == 'M' and
                    data[i + 1][j - 1] == data[i + 1][j + 1] == 'S'
                ):
                    ans += 1
                    continue
                if (
                    data[i + 1][j - 1] == data[i - 1][j - 1] == 'M' and
                    data[i + 1][j + 1] == data[i - 1][j + 1] == 'S'
                ):
                    ans += 1
                    continue
                if (
                    data[i + 1][j + 1] == data[i + 1][j - 1] == 'M' and
                    data[i - 1][j + 1] == data[i - 1][j - 1] == 'S'
                ):
                    ans += 1
                    continue
                if (
                    data[i - 1][j + 1] == data[i + 1][j + 1] == 'M' and
                    data[i - 1][j - 1] == data[i + 1][j - 1] == 'S'
                ):
                    ans += 1
                    continue

    print("Part 2 ans:", ans)


